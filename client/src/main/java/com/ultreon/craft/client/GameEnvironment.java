package com.ultreon.craft.client;

/**
 * Enum representing different game environments.
 */
public enum GameEnvironment {
    /**
     * Represents the packaged game environment.
     */
    PACKAGED,

    /**
     * Represents the development game environment.
     */
    DEVELOPMENT,

    /**
     * Represents the normal game environment.
     */
    NORMAL,

    /**
     * Represents an unknown game environment.
     */
    UNKNOWN
}
